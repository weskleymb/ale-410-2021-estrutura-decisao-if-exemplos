public class Exemplo02 {

    /*
    Estará aprovado o que estiver com a
    nota acima ou igual a 7.0
     */

    public static void main(String[] args) {

        Double nota = 6.9;

        Boolean aprovado = nota >= 7.0;

        if (aprovado) {
            System.out.println("Você está APROVADO!!! \\o/");
        }

    }

}
